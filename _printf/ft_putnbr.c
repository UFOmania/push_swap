#include "ft_printf.h"

int	ft_putnbr(int nbr)
{
	int	digits;

	digits = nbr;
	if (nbr == -2147483648)
		ft_putstr("-2147483648");
	else if (nbr < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nbr);
	}
	else if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putchar((nbr % 10) + '0');
	}
	else
		ft_putchar((nbr % 10) + '0');
	return (ft_digits_count(digits));
}
